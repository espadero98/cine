    private void lblAgregarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_lblAgregarMouseClicked
        String id=txtCodigoActor.getText();
        String ac=txtActor.getText();
        
        
        
        if (id.isEmpty() && ac.isEmpty()) {
            JOptionPane.showMessageDialog(null, "rellene todos los campos por favor");
        }else{
            try{
                Clasificacion actor=new Clasificacion();
                actor.setIdClasificacion(id);
                actor.setClasificacion(ac);
                ControladorClasificacion.Agregar(actor);
                    JOptionPane.showMessageDialog(null,"Todo se ejecuto con exito");
                } catch (ErrorPelicula ex) {
                    Logger.getLogger(frmClasificacionAgregar.class.getName()).log(Level.SEVERE, null, ex);
                }
             
            }
            
            
            
        
    }//GEN-LAST:event_lblAgregarMouseClicked

